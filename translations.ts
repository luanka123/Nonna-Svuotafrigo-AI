const getLang = (): 'it' | 'en' => {
  if (typeof navigator !== 'undefined' && navigator.language.startsWith('it')) {
    return 'it';
  }
  return 'en';
};

export const lang = getLang();

export const t = (key: keyof typeof translations.en): string => {
  return translations[lang][key] || translations.en[key];
};